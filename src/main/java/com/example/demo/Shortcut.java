package com.example.demo;

public class Shortcut {
    /*
        ===== 代码生成 =====
        生成main方法： psvm
        If null: ifn, If not null: inn
        public static final: psf,
        public static final int: psti, public static final String: psfs
        list.fori, list.for, list.forr(逆序遍历)
        代码补全： CTRL+空格（缺省，跟输入法有冲突，可以重新设置：file->settings->keymap 查找code completion）  ALT+/ (当前)
        自动生成代码： ALT+INSERT
        自动补全后面代码：CTRL+SHIFT+ENTER (右引号右括号，分号)
        自动代码生成模板： CTRL+J （psvm：生成main方法, sout: System.out.print, fori: for循环）
        迭代某个集合：集合变量名+for，比如：users.for 回车
        方法快速赋值变量：CTRL+ALT+V
        ===== 代码优化 =====
        导入类/包：ALT+ENTER
        格式化代码：CTRL+ALT+L(跟QQ设置有冲突，可以干掉QQ的)
        自动缩进： CTRL+ALT+I
        显示最近修改的文件： CTRL+E
        方法参数提示： CTRL+P
        把选中代码嵌入if for do...while 等语句里边： CTRL+ALT+T

        ===== 代码编辑 =====
        复制行： CTRL+D
        删除行： CTRL+Y
        自动选中代码/行： CTRL+W  相反操作(反选代码)：CTRL+SHIFT+W
        字母大小写转换：CTRL+SHIFT+U

        ===== DEBUG快捷键 =====
        File->Settings->Build,Ex...->Debugger Transport选Shared Memory
        https://www.bilibili.com/video/BV1PW411X75p?p=17
        RUN当前类：  SHIFT+F10 , RUN...   : ALT+SHIFT+F10
        DEBUG当前类：SHIFT+F9    DEBUG... : ALT+SHIFT+F9
        STOP: CTRL+F2   RESUME: F9
        Step Into: F7(自己写的方法？)   Force Step Into: ALT+SHIFT+F7 (别人写的方法？)   Step Over: F8   Step Out: SHIFT+F8
        调式模式查看表达式值：ALT+F8
        StepInto方法入栈，StepOut方法出栈

        ===== 注释快捷键 =====
        注释选中的行： CTRL+/ (单行注释符)  CTRL+SHIFT+/ (多行注释符)

        ===== 查找快捷键 =====
        查找类： CTRL+N
        查找文件：CTRL+SHIFT+N
        跳转代码到某行： CTRL+G
        在当前文件中查找文本： CTRL+F
        在当前文件中替换：CTRL+R
        光标在不同方法上下移动： CTRL+上箭头/下箭头
    */
}
